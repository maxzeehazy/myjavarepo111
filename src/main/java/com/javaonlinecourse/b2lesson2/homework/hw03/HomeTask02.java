package com.javaonlinecourse.b2lesson2.homework.hw03;

/**
 *  Написать четыре метода void print с разными параметрами.
 *  Метод использует System.out.println()
 */
public class HomeTask02 {
    void print(int a) {
        System.out.println(a);
    }

    void print(double a) {
        System.out.println(a);
    }

    void print(char a) {
        System.out.println(a);
    }

    void print(String a) {
        System.out.println(a);
    }
}
