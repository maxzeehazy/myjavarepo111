public class Task03 {

 public static void main(String[] args) throws Exception
    {

        BankCard card = new ApplePay();
        System.out.println(card.getName());
        System.out.println(card.getVendor());

    }

    public static abstract class Vendor{
        public static final String APPLE_PAY = "APPLE_PAY";
    }

    public interface Transferable
    {
        String getVendor();
    }

    public static abstract class BankCard implements Transferable
    {
        public String getName() {
            return "Sberbank Visa";
        }
    }

    public static class ApplePay extends BankCard {
        public String getVendor () {
            return Vendor.APPLE_PAY;
        }
    }
}
